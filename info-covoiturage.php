    <h2 class="low">Qu'est-ce que le covoiturage ?</h2>
    <p>Le covoiturage est une solution de transport alternatif qui consiste à partager sa voiture avec d'autres personnes pour se rendre au même endroit. Cela permet de réduire les coûts de transport, de diminuer l'empreinte carbone et de créer du lien social.</p>

    <!-- Avantages du covoiturage -->
    <h2 class="low">Pourquoi choisir le covoiturage ?</h2>
    <ul>
      <li>Économie d'argent : en partageant les frais de carburant, vous pouvez réaliser des économies conséquentes sur vos déplacements.</li>
      <li>Écologique : en partageant votre voiture, vous réduisez les émissions de gaz à effet de serre et contribuez à la lutte contre le changement climatique.</li>
      <li>Social : en covoiturant, vous avez l'opportunité de rencontrer de nouvelles personnes et de créer du lien social.</li>
    </ul>

